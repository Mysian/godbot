// ==== commands/godbit-guide.js ====
const { SlashCommandBuilder, EmbedBuilder } = require('discord.js');

module.exports = {
  data: new SlashCommandBuilder()
    .setName('갓비트안내')
    .setDescription('갓비트 시스템의 모든 기능과 룰을 한눈에 안내합니다!'),

  async execute(interaction) {
    const embed = new EmbedBuilder()
      .setTitle("📖 갓비트 모의 주식/코인 시스템 안내")
      .setDescription(
`**• 시장/코인**
- 까리한 디스코드만의 모의 주식(코인) 게임입니다. 모든 코인은 1분마다 가격이 자동 변동하며, 까리코인 시세가 전체 시장의 변동성에 영향을 미칩니다.
- '까리코인' 외에는 서버 멤버 닉네임 2글자 기반 자동 신규 상장/폐지(저가+폭락시) 기능이 적용됩니다.

**• 거래**
- \`/갓비트 매수\`와 \`/갓비트 매도\`로 모든 코인 자유롭게 사고팔 수 있습니다.
- 거래 시 파랑 정수(BE)가 소모되며, 매수/매도 각각 수수료가 부과됩니다.

**• 차트/히스토리**
- \`/갓비트 코인차트\`로 전체 시장/원하는 코인 실시간 차트, 현황, 최대 매수 개수 확인.
- \`/갓비트 히스토리\`로 특정 코인의 가격 변동 이력(최대 100회), 상장/폐지 시점까지 조회 가능.

**• 내 보유/수익**
- \`/갓비트 내코인\` 명령어로 내가 보유한 모든 코인의 보유량, 누적 매수, 평가액, 실시간 손익/수익률 확인 가능.

**• 추가 시스템**
- 각 코인은 거래량, 특정 시간대/요일, 이름별 특성, 까리코인과의 상관관계, 우상향/우하향 장기 흐름 등 다양한 변수로 실시간 변동.
- 시장/시세는 100% 자동 갱신됩니다. 시세 급락으로 상장폐지된 코인은 새 멤버 닉네임/랜덤 이름으로 자동 신규상장됩니다.

**• 주의사항**
- 실제 현금 손실은 없으며, 변동성이 크므로 투자 전략 실험과 재미에 집중하세요!
- 일부 코인은 거래량이 많거나 특정 시간대/요일에 더 크게 움직일 수 있습니다.
`
      )
      .setColor('#2095ff')
      .setFooter({ text: "/갓비트 코인차트, 매수, 매도, 내코인, 히스토리 등 명령어 참고" });

    await interaction.reply({ embeds: [embed], ephemeral: true });
  }
};
